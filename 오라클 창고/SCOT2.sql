SELECT * FROM DEPT;

SELECT DNAME, AVG(SAL)
    FROM DEPT D
    JOIN EMP E USING(DEPTNO)
    WHERE E.DEPTNO = D.DEPTNO
    GROUP BY DNAME;

SELECT DEPTNO, ROUND(AVG(SAL))
    FROM EMP
    WHERE SAL > 2000
    GROUP BY DEPTNO;
    
SELECT DEPTNO, ROUND(AVG(SAL))
